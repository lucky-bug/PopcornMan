     */
    public function show(Genre $genre)
    {
        $movies = $genre->movies();
        if (request()->input('q')) {
            $movies = $genre->movies()->where('title', 'LIKE', '%' . request()->input('q') . '%');
        }

        return view('movies.list', [
            'list' => $genre->movies()->pluck('title'),
            'movies' => $movies->paginate(env('PEGINATION_SIZE', 4)),
        ]);
    }

